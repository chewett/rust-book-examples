fn main() {
    let number = 8;
    if number % 5  == 0{
        println!("Number divisible by 5");
    }else if number % 3 == 0 {
        println!("Number divisible by 3");
    }else if number % 2 == 0 {
        println!("Number divisible by 2");
    }else{
        println!("Number not divisible by 5, 3, or 2");
    }



}
